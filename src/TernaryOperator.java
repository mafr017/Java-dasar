public class TernaryOperator {
    public static void main(String[] args) {
        /** Ternary Operator
         * operator sederhana dari if statement
         */
        var nilai = 65;
        String ucapan = nilai >= 70 ? "Selamat Anda Lulus!" : "Anda Tidak Lulus!";
        System.out.println(ucapan);
    }
}
