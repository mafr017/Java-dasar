public class TipeDataCharacter {
    public static void main(String[] args) {
        /**
         * char menggunakan (' ')
         * string menggunakan (" ")
         */
        char a = 'A';
        char d = 'd';
        char i = 'i';
        char t = 't';
        char y = 'y';

        String aditya = "Aditya";

        System.out.print(a);
        System.out.print(d);
        System.out.print(i);
        System.out.print(t);
        System.out.print(y);
        System.out.println(a);
        System.out.println(aditya);
    }
}
